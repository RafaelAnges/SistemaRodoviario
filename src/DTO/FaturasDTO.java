public class FaturasDTO {
    private String dataInicio;
    private String dataFim;
    private String dataSaida_p;
    private int valor_p;
    private int id;

    /**
     * @return the dataInicio
     */
    public String getDataInicio() {
        return dataInicio;
    }

    /**
     * @param dataInicio the dataInicio to set
     */
    public void setDataInicio(String dataInicio) {
        this.dataInicio = dataInicio;
    }

    /**
     * @return the dataFim
     */
    public String getDataFim() {
        return dataFim;
    }

    /**
     * @param dataFim the dataFim to set
     */
    public void setDataFim(String dataFim) {
        this.dataFim = dataFim;
    }

    /**
     * @return the dataSaida_p
     */
    public String getDataSaida_p() {
        return dataSaida_p;
    }

    /**
     * @param dataSaida_p the dataSaida_p to set
     */
    public void setDataSaida_p(String dataSaida_p) {
        this.dataSaida_p = dataSaida_p;
    }

    /**
     * @return the valor_p
     */
    public int getValor_p() {
        return valor_p;
    }

    /**
     * @param valor_p the valor_p to set
     */
    public void setValor_p(int valor_p) {
        this.valor_p = valor_p;
    }

    /**
     * @return the id
     */
    public int getId() {
        return id;
    }

    /**
     * @param id the id to set
     */
    public void setId(int id) {
        this.id = id;
    }
    
    
    
    
    
    
    
    
    
}
